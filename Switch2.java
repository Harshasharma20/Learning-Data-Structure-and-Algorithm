class Switch2{
	public static void main(String[] args){
		int a=50;
		switch(a){
			case 150:
				System.out.println("a = 150 is executed");
				break;
			case 100:
				System.out.println("a = 100 is  executed");
				break;
			case 50:
				System.out.println("a = 50 is executed");
			default:
				System.out.println("Default is executed");
		}
	}
}